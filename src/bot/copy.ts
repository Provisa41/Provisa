/** Stub AI readiness score for V1 demo */
export function mockDocumentScore(fileName: string): {
  score: number;
  summary: string;
  issues: string[];
} {
  const seed = fileName.length % 3;
  const scores = [72, 85, 91];
  const score = scores[seed] ?? 80;
  return {
    score,
    summary:
      score >= 85
        ? "Документы выглядят готовыми к подаче. Проверьте срок действия паспорта."
        : "Есть замечания — исправьте перед записью в консульство.",
    issues:
      score >= 85
        ? []
        : ["Проверьте читаемость скана", "Убедитесь, что все страницы загружены"],
  };
}
